    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(col.getColor()==0)
        {
            JOptionPane.showMessageDialog(null, "Выберите цвет!");
            return;
        }
        if(model.getAllQtyRazmeri()<=0)
        {
            JOptionPane.showMessageDialog(null, "Введите количество по размерам!");
            return;
        }
        pp = model.saveMPlan(mod.getModel(), jDateChooser3.getDate(), col.getColor(), jDateChooser4.getDate(), jTextField3.getText(), jDateChooser5.getDate());        
        result = true;
        setVisible(false);        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1ComponentResized(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_jTable1ComponentResized
        // TODO add your handling code here:
        if(jTable1.getColumnCount()<=0)
            return;
        int h = jTable1.getWidth()/2;
        
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(0);
        jTable1.getColumnModel().getColumn(0).setMaxWidth(0);
        jTable1.getColumnModel().getColumn(0).setMinWidth(0);
        
        jTable1.getColumnModel().getColumn(1).setPreferredWidth(h);
        jTable1.getColumnModel().getColumn(2).setPreferredWidth(h);
    }//GEN-LAST:event_jTable1ComponentResized

