    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int h = jTable2.getSelectedRow();
        if(h==-1) return;
        FUpdateColorMPlan fu = new FUpdateColorMPlan(null, true, (int)jTable2.getValueAt(h, 0));
        fu.setVisible(true);
        if(fu.getResult())
        {
            model2.setData(mplan.getPmodelplan());
            jTable2.setRowSelectionInterval(h, h);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        int h = jTable1.getSelectedRow();
        if(h==-1) return;
        FGetQty fg = new FGetQty(null, true, (int)jTable1.getValueAt(h, 2), "Кол-во для размера " + (String)jTable1.getValueAt(h, 1));
        fg.setVisible(true);
        if(fg.getResult())
        {
            IOModelPlan.updateQtyPModelPlanRazmeri((int)jTable1.getValueAt(h, 0), fg.getQty());
            mplan = IOModelPlan.getPModelPlan(mplan.getPmodelplan());
            model.setDataForPModel(mplan.getPmodelplan());
            model2.setQty(mplan.getSumqty());            
            model2.setData(mplan.getPmodelplan());
            jTextField4.setText(mplan.getSumqty().toString());
            jTable1.setRowSelectionInterval(h, h);
        }
                
    }//GEN-LAST:event_jButton5ActionPerformed

