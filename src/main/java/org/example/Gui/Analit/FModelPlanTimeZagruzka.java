    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        int w = Toolkit.getDefaultToolkit ().getScreenSize().width-20;
        int h = Toolkit.getDefaultToolkit ().getScreenSize().height-100;
        setSize(w,h);              
        setLocationRelativeTo(null);
        
        
        Calendar c = Calendar.getInstance();        
        c.setTime(dSt);
        c.set(Calendar.DAY_OF_MONTH, 1);  
        
        jDateChooser1.setDate(c.getTime());
        
        c.add(Calendar.MONTH, 1);
        c.add(Calendar.DAY_OF_MONTH, -1);
        
        jDateChooser2.setDate(c.getTime());
        
        model.setData(mp, jDateChooser1.getDate(), jDateChooser2.getDate());
        jTable1.setModel(model);
        jTable1.setShowGrid(true);
        jTable1.getTableHeader().setReorderingAllowed(false);
        
        jTable1.getColumnModel().getColumn(2).setCellRenderer(HelpClass.jTableRendererColumnRight);
        jTable1.getColumnModel().getColumn(3).setCellRenderer(HelpClass.jTableRendererColumnRight);
        jTable1.getColumnModel().getColumn(4).setCellRenderer(HelpClass.jTableRendererColumnRight);
        
        jTable1ComponentResized(null);
        
        
    }//GEN-LAST:event_formWindowOpened

