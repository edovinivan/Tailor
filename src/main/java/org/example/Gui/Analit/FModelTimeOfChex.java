    private DefaultTableCellRenderer jRenderer = new DefaultTableCellRenderer() {
        @Override
        public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {

            JLabel cell = (JLabel) super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
            cell.setHorizontalAlignment(JLabel.LEFT);
            if(column>=2 )
               cell.setHorizontalAlignment(JLabel.RIGHT);
            
            if(!isSelected)
            {
                int y = (int)jTable1.getValueAt(row, 6);
                if(y == 1)
                    cell.setBackground(new Color(153,255,153));
                if(y == 0)
                    cell.setBackground(new Color(255,255,255));
                if(y == 2)
                    cell.setBackground(new Color(255,153,153));                    
                
            }
            return cell;
        }

    };
    
