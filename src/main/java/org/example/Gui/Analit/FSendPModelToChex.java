    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int qv = (int)jSpinner1.getValue();
        if(qv>pm.getNoChex())
        {
            JOptionPane.showMessageDialog(null, "Введите правильное значение количества.");
            return;
        }
        
        if(qv==0)
        {
            JOptionPane.showMessageDialog(null, "Введите правильное значение количества.");
            return;
        }
        
        PTimeChex p = new PTimeChex();
        p.setChex((RabotnikGruppa)jComboBox1.getSelectedItem());
        p.setPmodelplan(pm);
        p.setPtimechex(0);
        p.setQty(qv);
        result = IOTimeOpChex.savePTimeChex(p).getPtimechex()!=0;
        setVisible(false);                
    }//GEN-LAST:event_jButton1ActionPerformed

