    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        FGetColor fg = new FGetColor(null, true, 0, 1);
        fg.setVisible(true);
        if(fg.getResult())
        {            
            pp.setColor(IOColor.getListColor(fg.getResultValue(), 0).get(0));            
            jTextField2.setText(pp.getColor().getName());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        FGetProduct fg = new FGetProduct(null, true, 0, 800, 600, 1);
        fg.setVisible(true);
        if(fg.getResult())
        {
            pp.setProduct(fg.getProduct());
            jTextField1.setText(pp.getProduct().getName());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

