    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        int h = jTable1.getSelectedRow();
        if(h==-1) return;
        if (JOptionPane.showConfirmDialog(null, "Вы действительно хотите удалить задачу из закупки?", "Вопрос.", JOptionPane.YES_NO_OPTION) == 0) 
        {
            IOZakupki.delPZakupkiJournal((int)jTable1.getValueAt(h, 0));
            model1.setData(pz.getPzakupki());        
            model2.setData(pz.getPzakupki());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        FGetPModelPlan f = new FGetPModelPlan(null, true);
        f.setVisible(true);
        if(f.getResult())
        {
            PZakupkiJournal j = new PZakupkiJournal();
            j.setPmodelplan(new PModelPlan(f.getResultValue()));
            j.setPzakupki(pz);
            j.setPzakupkijournal(0);
            if(IOZakupki.setPZakupkiJournal(j).getPzakupkijournal()>0)
            {
                PModelPlan pm = IOModelPlan.getPModelPlan(f.getResultValue());
                pm.setStatus(2);
                IOModelPlan.setPModelPlan(pm);
            }
            
            model1.setData(pz.getPzakupki());        
            model2.setData(pz.getPzakupki());
        }
    }//GEN-LAST:event_jButton5ActionPerformed

