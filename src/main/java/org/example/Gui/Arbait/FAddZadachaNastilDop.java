    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!HelpClass.checkTextForInt(jTextField2.getText()))
        {
            JOptionPane.showMessageDialog(null, "Введите правильную длину.");
            return;
        }
        
        if(!HelpClass.checkTextForInt(jTextField3.getText()))
        {
            JOptionPane.showMessageDialog(null, "Введите правильное количество изделий в 1 слое.");
            return;
        }
        
        if(HelpClass.GetIntIzText(jTextField3.getText())==0)
        {
            JOptionPane.showMessageDialog(null, "Введите количество изделий в 1 слое.");
            return;
        }
        
        if(!HelpClass.checkTextForName(jTextField1.getText()))
        {
            JOptionPane.showMessageDialog(null, "Введите правильный размер");
            return;
        }
        
        
        result = true;
        setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

