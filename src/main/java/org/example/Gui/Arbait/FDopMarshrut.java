    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        zp = IOZadachaKomplektovkaPretenzia.getZadachaKomplektovkaPretenzia(z);
        
        jDateChooser1.setDate(zp.getDate1());
        jDateChooser2.setDate(zp.getDateget());
        jDateChooser3.setDate(zp.getDatereturn());
        jDateChooser4.setDate(zp.getDatesclad());
        jTextField1.setText(zp.getZadachakomplektovka().getModel().getArticle());
        jSpinner2.setValue(zp.getCounts());
        jSpinner1.setValue(zp.getQty());
        jSpinner3.setValue(zp.getScladqty());
        
        setLocationRelativeTo(null);
        
        //если приняли в цех
        if(zp.getStatus()>5)
        {
            jDateChooser3.setEnabled(false);
            jSpinner1.setEnabled(false);
        }
        
        if(zp.getStatus()>6)
        {
            jDateChooser4.setEnabled(false);
            jSpinner3.setEnabled(false);
        }
    }//GEN-LAST:event_formWindowOpened

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //если только принят в цех
        if(zp.getStatus()==5)
        {
            zp.setDatereturn(jDateChooser3.getDate());
            zp.setQty((int)jSpinner1.getValue());
        }
        if(zp.getStatus()==6)
        {
            zp.setDatesclad(jDateChooser4.getDate());
            zp.setScladqty((int)jSpinner3.getValue());
        }
        IOZadachaKomplektovkaPretenzia.setZadachaKomplektovkaPretenzia(zp);
        setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

