    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
        ZadachaKomplektovkaPretenzia zp = IOZadachaKomplektovkaPretenzia.getPretenziaForZadachaKomplektovka(mar.getZadachakomplektovka());
        if(zp.getZkpretenzia()!=0)
        {
            if (JOptionPane.showConfirmDialog(null, "У данного маршрута уже есть претензия. Создать новую?", "Вопрос.", JOptionPane.YES_NO_OPTION) == 0) 
            {
                FPretenzia fp = new FPretenzia(null, true, 0, mar.getZadachakomplektovka());
                fp.setVisible(true);                
                return;
            }            
            FPretenzia fp = new FPretenzia(null, true, zp.getZkpretenzia(), mar.getZadachakomplektovka());
            fp.setVisible(true);                
            return;
        }
        FPretenzia fp = new FPretenzia(null, true, 0, mar.getZadachakomplektovka());
        fp.setVisible(true);
        
    }//GEN-LAST:event_jButton4ActionPerformed

