    private void setStatus()
    {
        jTextField7.setEditable(false);
        switch(mar.getStatus())
        {
            case 2: // сформировали комплектовку
                jDateChooser3.setEnabled(false);                
                jDateChooser5.setEnabled(false);
                jDateChooser6.setEnabled(false);
                jSpinner1.setEnabled(false);
                jTextField4.setEditable(false);
                jSpinner2.setEnabled(false);
                jSpinner4.setEnabled(false);
                jTextField6.setEditable(false);
                jSpinner5.setEnabled(false);
                break;
            case 3: // отправили в цех
                jDateChooser2.setEnabled(false);                
                //jDateChooser3.setEnabled(false);                
                jDateChooser4.setEnabled(false);                
                jDateChooser5.setEnabled(false);
                jDateChooser6.setEnabled(false);
                jSpinner1.setEnabled(false);
                jTextField4.setEditable(false);
                jSpinner2.setEnabled(false);
                jButton3.setEnabled(false);   
                jSpinner4.setEnabled(false);
                jTextField6.setEditable(false);
                jSpinner5.setEnabled(false);
                                
                /*if(IOOperator.getSetupOperator(10)>0)
                {
                    
                }*/
                break;    
            case 4: // получили в цехе
                jDateChooser2.setEnabled(false);                
                jDateChooser3.setEnabled(false);                
                jDateChooser4.setEnabled(false);                
                //jDateChooser5.setEnabled(false);
                jDateChooser6.setEnabled(false);
                //jSpinner1.setEnabled(false);
                //jTextField4.setEditable(false);
                jSpinner2.setEnabled(false);
                jButton3.setEnabled(false);                
                break;        
            case 5: // выполнили в цехе
                jDateChooser2.setEnabled(false);                
                jDateChooser3.setEnabled(false);                
                jDateChooser4.setEnabled(false);                
                jDateChooser5.setEnabled(false);
                //jDateChooser6.setEnabled(false);
                jSpinner1.setEnabled(false);
                jTextField4.setEditable(false);
                
                jSpinner4.setEnabled(false);
                jTextField6.setEditable(false);
                //jSpinner2.setEnabled(false);
                jButton3.setEnabled(false);   
                jSpinner5.setEnabled(false);
                break;            
            case 6: // Приняли на склад
                jDateChooser2.setEnabled(false);                
                jDateChooser3.setEnabled(false);                
                jDateChooser4.setEnabled(false);                
                jDateChooser5.setEnabled(false);
                jDateChooser6.setEnabled(false);
                jSpinner1.setEnabled(false);
                jTextField4.setEditable(false);
                jSpinner2.setEnabled(false);
                jButton3.setEnabled(false); 
                jSpinner4.setEnabled(false);
                jTextField6.setEditable(false);
                jSpinner5.setEnabled(false);
                break;                
        }
    }

