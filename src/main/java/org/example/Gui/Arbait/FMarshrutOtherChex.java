    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        FGetRabotnikGruppa fg = new FGetRabotnikGruppa(null, true, 0);
        fg.setVisible(true);
        if(fg.getResult())
        {
            newChex = IORabotnik.getListRabotnikGruppa(fg.getResultValue()).get(0);
            if(newChex.getRabotnikgruppa() == zk.getChek().getRabotnikgruppa()){
                JOptionPane.showMessageDialog(null, "Нельзя перемещать в тот же цех!");
                newChex = new RabotnikGruppa(0);
                return;
            }
            jTextField3.setText(newChex.getName());          
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(newChex.getRabotnikgruppa()==0){
            JOptionPane.showMessageDialog(null, "Выберите цех получатель!");
            return;
        }
        
        if (newChex.getRabotnikgruppa() == zk.getChek().getRabotnikgruppa()) {
            JOptionPane.showMessageDialog(null, "Нельзя перемещать в тот же цех!");
            return;
        }
        
        IOZLog.setZLog(zk.getZadacha().getZadacha(), zk.getZadachakomplektovka(), 3, "Переместили комплектовку с " + zk.getChek().getName() + " в " + newChex.getName());
        
        zk.setChek(newChex);
        IOZadachaKomplektovka.setZadachaKomplektovka(zk);
        
        result = true;
        setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

