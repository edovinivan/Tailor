    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        int w,h;
        w = Toolkit.getDefaultToolkit ().getScreenSize().width-20;
        h = Toolkit.getDefaultToolkit ().getScreenSize().height-50;
        setSize(w,h);      
        setLocationRelativeTo(null);
        
        
        jDateChooser1.setDate(new Date());
        jDateChooser2.setDate(new Date());
        jDateChooser3.setDate(new Date());
        jDateChooser1.setVisible(false);
        jDateChooser2.setVisible(false);
        jDateChooser3.setVisible(false);
        jLabel3.setVisible(false);
        jLabel4.setVisible(false);
        jLabel5.setVisible(false);
      
        
        jButton1.setVisible(false);
        jButton2.setVisible(false);
        jButton3.setVisible(false);
        jButton4.setVisible(false);
        jButton5.setVisible(false);        
        jButton6.setVisible(false);
        
        if(zkp.getZkpretenzia()!=0)
        {
            jDateChooser1.setDate(zkp.getDate1());
            jDateChooser2.setDate(zkp.getDate2());
            jDateChooser3.setDate(zkp.getDate3());            
            
            //если отправили вопрос
            if(zkp.getStatus()>1)
            {
                jLabel3.setVisible(true);
                jDateChooser1.setVisible(true);
                
            }
            // если прочитали ответ
            if(zkp.getStatus()>2)
            {
                jLabel4.setVisible(true);
                jDateChooser2.setVisible(true);
                //model.setEditRom5(true);
            }
            // если сделали ответ
            if(zkp.getStatus()>3)
            {
                jLabel5.setVisible(true);
                jDateChooser3.setVisible(true);
              
            }
            // редактирование
            if(zkp.getStatus()<2)
            {
                jButton1.setVisible(true);
                jButton2.setVisible(true);
                jButton3.setVisible(true);
                //model.setEditRom3_4(true);
            }
            // составили вопрос
            if(zkp.getStatus()==0)
            {
                model.setEditRom3_4(true);
            }
            // составили вопрос
            if(zkp.getStatus()==1)
            {
                model.setEditRom3_4(true);
            }
            // отправили вопрос
            if(zkp.getStatus()==2)
            {
                //jButton1.setVisible(true);
                jButton5.setVisible(true);
                jButton4.setVisible(true);  
                jButton6.setVisible(true);
                model.setEditRom5(true);
             
            }
            // приняли к сведению
            if(zkp.getStatus()==3)
            {                
                jButton5.setVisible(true);
                jButton4.setVisible(true);
                jButton6.setVisible(true);
                model.setEditRom5(true);
               
            }
            // ответили
            if(zkp.getStatus()==4)
            {
                jButton3.setVisible(true);
            
            }
            model.setData(zkp.getZkpretenzia());
            
            jTable1.setModel(model);
            jTable1.setShowGrid(true);                    
            jTable1.getTableHeader().setReorderingAllowed(false);  
            jTable1ComponentResized(null);
            
        }
        /*else
        {
            zkp.setZadachakomplektovka(IOZadachaKomplektovka.getListZadachaKomplektovka(0, zk1).get(0));
            zkp.setStatus(1);   
            jButton1.setVisible(true);
            jButton2.setVisible(true);
            jButton3.setVisible(true);
        }*/
        jTextField1.setText(zkp.getZadachakomplektovka().getNomer());
        jTextField2.setText(zkp.getZadachakomplektovka().getModel().getArticle());
        //если нет права на создание претензий
        if(IOOperator.getSetupOperator(44)<2)
        {
            jButton1.setVisible(false);
            jButton2.setVisible(false);            
        }
        // если нет право для ответа претензии
        if(IOOperator.getSetupOperator(45)<2)
        {
            jButton4.setVisible(false);
            jButton5.setVisible(false);            
        }
    }//GEN-LAST:event_formWindowOpened

