    // отправить вопрос
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (JOptionPane.showConfirmDialog(null, "Вы действительно хотите отправить претензию?", "Вопрос.", JOptionPane.YES_NO_OPTION) == 0) 
        {
           
            zkp.setDate1(new Date());
            zkp.setDate2(new Date());
            zkp.setDate3(new Date());
          
            zkp.setStatus(2);
            
            IOZadachaKomplektovkaPretenzia.delNullPretenziaRazmer(zkp.getZkpretenzia());
          
            IOZadachaKomplektovkaPretenzia.setZadachaKomplektovkaPretenzia(zkp);
            setVisible(false);

        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

