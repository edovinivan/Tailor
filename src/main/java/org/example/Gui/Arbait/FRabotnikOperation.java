    private void selectGrossSumm()
    {
        int v_op = (int)jSpinner2.getValue();
        int v_tm = (int)jSpinner1.getValue();
        BigDecimal s_op, s_pric;
        BigDecimal s_tm;
        s_pric = ro.getTimeo().multiply(ro.getPriceo()).divide(new BigDecimal(3600),6, RoundingMode.HALF_UP).setScale(2, RoundingMode.HALF_UP);
        //s_pric = new BigDecimal(1).divide(new BigDecimal(3600),6, RoundingMode.HALF_UP).multiply(ro.getTimeo()).multiply(ro.getPriceo()).setScale(2, RoundingMode.HALF_UP);
        //System.out.println(s_pric.toString());
        //s_op = new BigDecimal(1).divide(new BigDecimal(3600),10, RoundingMode.HALF_UP).multiply(ro.getTimeo()).multiply(ro.getPriceo()).multiply(new BigDecimal(v_op));
        s_op = s_pric.multiply(new BigDecimal(v_op));
        s_tm = ro.getPricer().divide(new BigDecimal(3600),6, RoundingMode.HALF_UP).multiply(new BigDecimal(v_tm));
        //System.out.println("OP = "+ s_op);
        //System.out.println("TM = "+ s_tm);
        if(s_op.compareTo(s_tm)<=0)
            ro.setSummaall(s_tm);
        else
            ro.setSummaall(s_op);
        jTextField7.setText(ro.getSummaall().setScale(2, RoundingMode.HALF_UP).toString());
    }
    
    
