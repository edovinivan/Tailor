    private boolean printSS()
    {
        if(!HelpClass.checkTextForInt(jTextField5.getText()))
        {
            JOptionPane.showMessageDialog(null, "Введите правильное количество сек.");
            return false;
        }
        int ss = HelpClass.GetIntIzText(jTextField5.getText());
        
        if(ss<1)
        {
            JOptionPane.showMessageDialog(null, "Введите правильное количество сек.");
            return false;
        }
        
        //если свободная цена
        if(price_null)
        {
            if(!HelpClass.checkTextForBigDecimal(jTextField4.getText()))
            {
                JOptionPane.showMessageDialog(null, "Введите правильную цену!");
                return false;
            }
            ro.setPricer(HelpClass.GetBigDecimalIzText(jTextField4.getText()));
            if(ro.getPricer().signum()<=0)
            {
                JOptionPane.showMessageDialog(null, "Введите правильную цену!");
                return false;
            }
        }        
        
        BigDecimal ssm =  ro.getPricer().divide(new BigDecimal(3600),6, RoundingMode.HALF_UP).multiply(new BigDecimal(ss));
        jTextField2.setText(ssm.setScale(2, RoundingMode.HALF_UP).toString());
        ro.setSummaall(ssm);
        ro.setTimes(ss);
        return true;
    }
    
