    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!HelpClass.checkTextForBigDecimal(jTextField5.getText()))
        {
            JOptionPane.showMessageDialog(null, "Введите правильную длину.");
            return;
        }
        BigDecimal w = HelpClass.GetBigDecimalIzText(jTextField5.getText());
        if (w.signum()==0) 
        {
            JOptionPane.showMessageDialog(null, "Введите правильную длину.");
            return;
        }
        if(w.compareTo(z.getScladproduct().getLight())>=0)
        {
            JOptionPane.showMessageDialog(null, "Введите правильную длину.");
            return;
        }
        result = IOZadachaNastil.reziceProduct(z.getZadachascladproduct(), HelpClass.GetBigDecimalIzText(jTextField5.getText()));
        setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

