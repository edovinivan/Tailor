        jTable1.getColumnModel().getColumn(4).setCellRenderer(model.jTableRenderer);
        jTable1.getColumnModel().getColumn(3).setCellRenderer(model.jTableRenderer);
                
        if(jTable1.getRowCount()>0)        
        {
            jTable1.changeSelection(0, 0, false, false);                
            model2.setData((int)jTable1.getValueAt(jTable1.getSelectedRow(), 0));
        }
        
        //model2.setData(zd.getZadacha());
        jTable2.setModel(model2);
        jTable2.setShowGrid(true);                    
