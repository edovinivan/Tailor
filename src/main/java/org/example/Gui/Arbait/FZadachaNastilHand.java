    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(zadachaNastil.getNastil().getRabotnik()==0)
        {
            JOptionPane.showMessageDialog(null, "Выберите настильщика");
            return;
        }
        if (zadachaNastil.getRezak().getRabotnik() == 0) {
            JOptionPane.showMessageDialog(null, "Выберите сотрудника резака!");
            return;
        }
        zadachaNastil.setQtyl((Integer) jSpinner1.getValue());
        if (zadachaNastil.getQtyl() == 0) {
            JOptionPane.showMessageDialog(null, "Введите количество слоев!");
            return;
        }
        
        if (zadachaNastil.getTipnastila().getTipnastila()==0) {
            JOptionPane.showMessageDialog(null, "Выберите тип настила");
            return;
        }
        
        if (zadachaNastil.getTipnastilania().getTipnastilania()==0) {
            JOptionPane.showMessageDialog(null, "Выберите тип настилания");
            return;
        }
        
        if (zadachaNastil.getWidth()==0) {
            JOptionPane.showMessageDialog(null, "Введите ширину настила.");
            return;
        }
        
        if(model.getAllQty().signum()==0)
        {
            JOptionPane.showMessageDialog(null, "Выберите полотна для настила");
            return;
        }
        
        saveNastil();
        int r;
        // если нет возвратов
        if(zadachaNastil.getQtyreturn().signum()==0)        
            r = IOZadachaNastil.setStatusOkForNastil(zadachaNastil.getZadachanastil(), 3);
        else
            r = IOZadachaNastil.setStatusOkForNastil(zadachaNastil.getZadachanastil(), 2);
            
        if(r==0)
            setVisible(false);
        else
            JOptionPane.showMessageDialog(null, "Ошибка утверждения. ");
        
    }//GEN-LAST:event_jButton2ActionPerformed

