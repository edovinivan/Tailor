    /**
     * Посчитать вес распределенных весов
     */
    private void printSummaQty()
    {
        BigDecimal sq = new BigDecimal(0);
        sq = sq.add(HelpClass.GetBigDecimalIzText(jTextField10.getText()));
        sq = sq.add(HelpClass.GetBigDecimalIzText(jTextField16.getText()));
        sq = sq.add(HelpClass.GetBigDecimalIzText(jTextField14.getText()));
        sq = sq.add(HelpClass.GetBigDecimalIzText(jTextField25.getText()));
        sq = sq.add(HelpClass.GetBigDecimalIzText(jTextField20.getText()));
        sq = sq.add(HelpClass.GetBigDecimalIzText(jTextField19.getText()));
        sq = sq.add(HelpClass.GetBigDecimalIzText(jTextField18.getText()));
        sq = sq.add(HelpClass.GetBigDecimalIzText(jTextField24.getText()));
        sq = sq.add(HelpClass.GetBigDecimalIzText(jTextField21.getText())); // вес бейки
        sq = sq.add(HelpClass.GetBigDecimalIzText(jTextField27.getText())); // лоскуты крупный
        jTextField6.setText(sq.setScale(2, RoundingMode.HALF_UP).toString());
        // неучтенный вес
        jTextField5.setText(HelpClass.GetBigDecimalIzText(jTextField13.getText()).subtract(sq).setScale(2, RoundingMode.HALF_UP).toString());
        if(sq.compareTo(HelpClass.GetBigDecimalIzText(jTextField13.getText()))!=0)
            jTextField6.setBackground(Color.red);
        else
            jTextField6.setBackground(new Color(240,240,240));
        
    }
