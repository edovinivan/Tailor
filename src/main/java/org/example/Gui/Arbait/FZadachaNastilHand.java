    private void jTextField17FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField17FocusLost
        // TODO add your handling code here:
        String s[];
        jTextField17.setBackground(Color.WHITE);
        try
        {
            s = jTextField17.getText().trim().split("-");
        }catch(PatternSyntaxException w)
        {
            jSpinner1.setValue(0);
            jTextField17.setBackground(Color.red);
            return;
        }
        int j, su=0;
        if(s.length>0)
        {
            for(String d: s)
            {
                try
                {
                    j = Integer.parseInt(d);
                }catch(NumberFormatException e){j = 0;jTextField17.setBackground(Color.red);}
                su = su + j;
            }
        }

        jSpinner1.setValue(su);
    }//GEN-LAST:event_jTextField17FocusLost

