    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int h = jTable1.getSelectedRow();
        if(h==-1)
            return;
        h = (int) jTable1.getValueAt(h, 0);
        if (JOptionPane.showConfirmDialog(null, "Вы действительно хотите вернуть комплектовку <" + jTable1.getValueAt(jTable1.getSelectedRow(), 2) + ">?", "Вопрос.", JOptionPane.YES_NO_OPTION) == 0) 
        {
            
            if(IOZadachaKomplektovka.setStatusKomplektovka(h, 1))
                model.setDataForGo();
            else
                JOptionPane.showMessageDialog(null, "Ошибка!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

