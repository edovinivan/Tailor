    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(model.getCountSelectRow()==0)
        {
            JOptionPane.showMessageDialog(null, "Нет выбранных маршрутов!");
            return;
        }
        
        FMarshrutGo fg = new FMarshrutGo(null, true);
        fg.setVisible(true);
        if(fg.getResult())
        {
            model.marshrutGo(fg.getChehGo(), fg.getDateGo(), fg.getDateReturn());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

