    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        int h = jTable1.getSelectedRow();
        if(h==-1)
            return;
        h = (int) jTable1.getValueAt(h, 0);
        if (JOptionPane.showConfirmDialog(null, "Вы действительно хотите переместить комплектовку <" + jTable1.getValueAt(jTable1.getSelectedRow(), 2) + "> на склад кроя?", "Вопрос.", JOptionPane.YES_NO_OPTION) == 0) 
        {
            ZadachaKomplektovka zz = IOZadachaKomplektovka.getListZadachaKomplektovka(0, h).get(0);
            if(zz.getStatus()!=1)
            {                
                return;
            }
            if(IOZadachaKomplektovka.setStatusKomplektovkaGut(h, 11))
            {
                IOZLog.setZLog(zz.getZadacha().getZadacha(), zz.getZadachakomplektovka(), 3, "Отправили комплектовку на склад кроя");
                model.setDataForKomplektovkaFurnitura();
            }
            else
                JOptionPane.showMessageDialog(null, "Ошибка перемещения");
        }
    }//GEN-LAST:event_jButton6ActionPerformed


