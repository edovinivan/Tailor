    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:    
        if(zadacha.getHand()==0)
        {
            FZadachaNastil fz = new FZadachaNastil(null, true, zadacha.getZadacha(), 0);
            fz.setSize(getWidth() - 50, getHeight() - 50);
            fz.setVisible(true);
        }
        else
        {
            FZadachaNastilHand fh = new FZadachaNastilHand(null, true, zadacha.getZadacha(), 0);
            fh.setSize(getWidth() - 50, getHeight() - 50);
            fh.setVisible(true);
        }
        model1.setData(zadacha.getZadacha());
        if(jTable1.getRowCount()>0)
        {
            jTable1.changeSelection(0, 0, false, false);                
            model2.setDataForNastil((int)jTable1.getValueAt(jTable1.getSelectedRow(), 0));
        }
    }//GEN-LAST:event_jButton2ActionPerformed

