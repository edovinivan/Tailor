    /**
     * Изменить 
     * @param evt 
     */
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int h = jTable1.getSelectedRow();
        if(h==-1) return;
        if(zadacha.getHand()==0)
        {
            FZadachaNastil fz = new FZadachaNastil(null, true, zadacha.getZadacha(), (int) jTable1.getValueAt(h, 0));
            fz.setSize(getWidth() - 50, getHeight() - 50);
            fz.setVisible(true);
        }
        else
        {
            FZadachaNastilHand fh = new FZadachaNastilHand(null, true, zadacha.getZadacha(), (int) jTable1.getValueAt(h, 0));
            fh.setSize(getWidth() - 10, getHeight() - 10);
            fh.setVisible(true);
        }
        model1.setData(zadacha.getZadacha());
        jTable1.changeSelection(h, 0, false, false);                
        model2.setDataForNastil((int)jTable1.getValueAt(jTable1.getSelectedRow(), 0));
    }//GEN-LAST:event_jButton3ActionPerformed

