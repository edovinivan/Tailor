    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        if(IOOperator.getSetupOperator(29)>1)
        {
            jButton2.setVisible(true);
            jButton3.setVisible(true);
            jButton4.setVisible(true);
            jButton5.setVisible(true);
            jButton6.setVisible(true);
        }
        
        if(IOOperator.getSetupOperator(48)>1)
        {
            jButton2.setVisible(true);
            jButton3.setVisible(true);
            jButton4.setVisible(true);
            jButton5.setVisible(true);
            jButton6.setVisible(true);
        }
        setLocationRelativeTo(null);
        jTextField1.setText(zadacha.getModel().getArticle() + " = " + zadacha.getModel().getName() );
        jDateChooser1.setDate(zadacha.getDates());
        jTextField2.setText(zadacha.getNomer());
        jTextField3.setText(zadacha.getText());
        //если задача закрыта на формирование настилов
        if(zadacha.getStatus()>2)
        {
            jButton2.setEnabled(false);
            //jButton3.setEnabled(false);
            jButton4.setEnabled(false);
            jButton5.setEnabled(false);
            jButton6.setEnabled(false);
            jButton8.setEnabled(false);
        }
        model1.setData(zadacha.getZadacha());
        jTable1.setModel(model1);
        jTable1.setShowGrid(true);                    
        jTable1.getTableHeader().setReorderingAllowed(false);   
        jTable1.getColumnModel().getColumn(1).setCellRenderer(model1.jTableRenderer1);
        jTable1ComponentResized(null);
        
        if(jTable1.getRowCount()>0)        
        {
            jTable1.changeSelection(0, 0, false, false);                
            model2.setDataForNastil((int)jTable1.getValueAt(jTable1.getSelectedRow(), 0));
        }
        
        
        jTable2.setModel(model2);
        jTable2.setShowGrid(true);                    
        jTable2.getTableHeader().setReorderingAllowed(false);   
        jTable2ComponentResized(null);
        jTable2.getColumnModel().getColumn(4).setCellRenderer(model2.jTableRenderer);
        jTable2.getColumnModel().getColumn(5).setCellRenderer(model2.jTableRenderer);
        jTable2.getColumnModel().getColumn(7).setCellRenderer(model2.jTableRenderer);
    }//GEN-LAST:event_formWindowOpened

