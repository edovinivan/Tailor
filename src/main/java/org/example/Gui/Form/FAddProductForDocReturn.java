    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        FGetScladProduct fg = new FGetScladProduct(null, true, dj.getScladproduct().getScladproduct(), 1, agent, jTextField1.getText(), 1);
        fg.setVisible(true);
        if(fg.getResult())
        {            
            ScladProduct sp = IOScladProduct.getScladProduct(fg.getResultValue());
            if(IODocReturn.testScladProductForDocReturn(sp.getScladproduct()))
            {
                JOptionPane.showMessageDialog(null, "Выбранное полотно уже присутствует в документах возврата поставщику.");
                return;
            }
            dj.setScladproduct(sp);           
            printSP();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        setLocationRelativeTo(null);
        if(dj.getDocreturnjournal()!=0)
        {
            dj = IODocReturn.getDorReturnJournal(dj.getDocreturnjournal());
            printSP();
        }
    }//GEN-LAST:event_formWindowOpened

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(dj.getScladproduct().getScladproduct()==0)
        {
            JOptionPane.showMessageDialog(null, "Выберите полотно");
            return;
        }
        dj.setText(jTextField8.getText());
        dj = IODocReturn.setDocReturnJournal(dj);
        result = dj.getDocreturnjournal()>0;
        setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

