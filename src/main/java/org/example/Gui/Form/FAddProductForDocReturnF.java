    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(j.getScladproduct().getScladproduct()==0)
        {
            JOptionPane.showMessageDialog(null, "Выберите фурнитуру");
            return;
        }
        if(!HelpClass.checkTextForBigDecimal(jTextField5.getText()))
        {
            JOptionPane.showMessageDialog(null, "Введите количество!");
            return;
        }
        j.setQty(HelpClass.GetBigDecimalIzText(jTextField5.getText()));
        j.setText(jTextField6.getText());
        j = IODocReturnF.setDocReturnFJournal(j);
        result = j.getDocreturnfjournal()>0;
        setVisible(false);
    }//GEN-LAST:event_jButton2ActionPerformed

