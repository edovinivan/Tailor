    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        FGetScladProduct fg = new FGetScladProduct(null, true, 0, 2, 0, jTextField1.getText(), 1);
        fg.setVisible(true);
        if(fg.getResult())
        {            
            ScladProduct sp = IOScladProduct.getScladProduct(fg.getResultValue());
            if(IODocReturnF.testScladProductForDocReturnF(sp.getScladproduct(), j.getDocreturnf().getDocreturnf()))
            {
                JOptionPane.showMessageDialog(null, "Выбранная фурнитура уже присутствует в документе возврата!");
                return;
            }
            j.setScladproduct(sp);           
            printSP();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

