    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jTextField1.getText() == null)
        {
            JOptionPane.showMessageDialog(null, "Введите название!");
            return;
        }
        if(jTextField1.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Введите название!");
            return;
        }
        if(jTextField1.getText().length()< 2)
        {
            JOptionPane.showMessageDialog(null, "Введите название!");
            return;
        }
        agent.setDel(0);
        agent.setName(jTextField1.getText());
        agent = IOAgent.setAgent(agent);
        result = agent.getAgent()>0;
        if(!result)
            JOptionPane.showMessageDialog(null, "Ошибка сохранения!");
        else
            setVisible(false);
                
    }//GEN-LAST:event_jButton1ActionPerformed

