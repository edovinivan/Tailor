    private void loadDR() {
        docr = IODocReturn.getDocReturn(docr.getDocreturn());
        jDateChooser1.setDate(docr.getDates());
        jTextField1.setText(docr.getNomer());
        jTextField2.setText(docr.getAgent().getName());
        if(docr.getStatus()==0)
        {
            jButton4.setEnabled(true);
            jButton5.setEnabled(true);
            jButton6.setEnabled(true);
            jButton7.setEnabled(true);
        }        
        if(docr.getStatus()==1)
            jButton1.setEnabled(false);
    }
