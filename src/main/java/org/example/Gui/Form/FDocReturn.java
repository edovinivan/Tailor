    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!HelpClass.checkTextForName(jTextField1.getText()))
        {
            JOptionPane.showMessageDialog(null, "Введите номер!");
            return;
        }
        
        if(docr.getAgent().getAgent() == 0)
        {
            JOptionPane.showMessageDialog(null, "Ваберите поставщика!");
            return;
        }
        
        docr.setNomer(jTextField1.getText());
        
        docr.setDates(jDateChooser1.getDate());        
        docr.setStatus(0);                        
        int ad = docr.getDocreturn();
        docr= IODocReturn.setDocReturn(docr);
        if(ad == 0)
        {
            if(docr.getDocreturn()==0)
            {
                JOptionPane.showMessageDialog(null, "Ошибка сохранения!");
                return;
            }
            jButton4.setEnabled(true);
            jButton5.setEnabled(true);
            jButton6.setEnabled(true);
            jButton7.setEnabled(true);
            return;
        }
        
        if(!(docr.getDocreturn()>0))
            JOptionPane.showMessageDialog(null, "Ошибка сохранения!");
        else
            setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

