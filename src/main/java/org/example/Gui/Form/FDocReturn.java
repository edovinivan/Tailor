    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        int h = jTable1.getSelectedRow();
        if(h==-1)
            return;
        h = (int) jTable1.getValueAt(h, 0);
        if (JOptionPane.showConfirmDialog(null, "Вы действительно хотите удалить полотно<" + jTable1.getValueAt(jTable1.getSelectedRow(), 2) + ">?", "Вопрос.", JOptionPane.YES_NO_OPTION) == 0) 
        {
            if(IODocReturn.delDocReturnJournal(h))
                model.setData(docr.getDocreturn());
            else
                JOptionPane.showMessageDialog(null, "Ошибка удаления");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        FMultiSelectScladProduct f = new FMultiSelectScladProduct(null, true);
        f.setVisible(true);
        if(f.getResult())
        {
            DocReturnJournal dj;
            int i=0;
            for(ScladProduct s: f.getSelectList())
            {
                dj = new DocReturnJournal();
                dj.setDocreturn(docr);
                dj.setDocreturnjournal(0);
                dj.setScladproduct(s);
                dj.setText("");
                IODocReturn.setDocReturnJournal(dj);
                i++;
                if(i>4)
                    break;
            }
            model.setData(docr.getDocreturn());
        }
    }//GEN-LAST:event_jButton7ActionPerformed

