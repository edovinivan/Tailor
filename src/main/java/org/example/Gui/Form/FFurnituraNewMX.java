    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        FGetSclad fg = new FGetSclad(null, true, scladProduct.getSclad().getSclad());
        fg.setVisible(true);
        if(fg.getResult())
        {
            scladProduct.setSclad(IOSclad.getListSclad(fg.getResultValue()).get(0));
            jTextField4.setText(scladProduct.getSclad().getName());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

