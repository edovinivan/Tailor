    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        FGetModel fg= new FGetModel(null, true, mcomment.getModel().getModel());
        fg.setSize(getWidth(), getHeight());
        fg.setVisible(true);
        if(fg.getResult())
        {            
            mcomment.setModel(IOModel.getListModel(fg.getResultValue(), -1, false).get(0));
            jTextField1.setText(mcomment.getModel().getArticle());            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (mcomment.getStatus() == 0) 
        {
            if (mcomment.getModel().getModel() == 0) {
                JOptionPane.showMessageDialog(null, "Выберите артикул");
                return;
            }
            if (!HelpClass.checkTextForName(jTextField2.getText())) {
                JOptionPane.showMessageDialog(null, "Введите комментарий");
                return;
            }
            if (jTextField2.getText().length() < 2) {
                JOptionPane.showMessageDialog(null, "Введите комментарий");
                return;
            }
            if (!HelpClass.checkTextForName(jTextField3.getText())) {
                JOptionPane.showMessageDialog(null, "Введите автора комментария");
                return;
            }
            if (jTextField3.getText().length() < 2) {
                JOptionPane.showMessageDialog(null, "Введите автора комментария");
                return;
            }
            mcomment.setTexts(jTextField2.getText());
            mcomment.setAvtor1(jTextField3.getText());
            mcomment.setStatus(1);
            IOMProperty.setMModelComment(mcomment);
        }
        else
        {
            // если это ответ
            if (mcomment.getStatus() == 1) 
            {
                if (!HelpClass.checkTextForName(jTextField4.getText())) 
                {
                    JOptionPane.showMessageDialog(null, "Введите ответ на комментарий");
                    return;
                }
                if (jTextField4.getText().length() < 2) 
                {
                    JOptionPane.showMessageDialog(null, "Введите ответ на комментарий");
                    return;
                }
                if (!HelpClass.checkTextForName(jTextField5.getText())) 
                {
                    JOptionPane.showMessageDialog(null, "Введите автора ответа");
                    return;
                }
                if (jTextField5.getText().length() < 2) 
                {
                    JOptionPane.showMessageDialog(null, "Введите автора ответа");
                    return;
                }
                mcomment.setAnsver(jTextField4.getText());
                mcomment.setAvtor2(jTextField5.getText());
                mcomment.setDates1(new Date());
                mcomment.setStatus(2);
                IOMProperty.setMModelComment(mcomment);
            }
        }
        
        setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        setLocationRelativeTo(null);
        jTextField4.setEnabled(false);
        jTextField5.setEnabled(false);
        
        if(mcomment.getMmodelcomment()>0)
        {
            mcomment = IOMProperty.getMModelComment(mcomment.getMmodelcomment());
            jTextField2.setText(mcomment.getTexts());
            jTextField1.setText(mcomment.getModel().getArticle());                        
            jTextField3.setText(mcomment.getAvtor1());
            jTextField4.setText(mcomment.getAnsver());         
            jTextField5.setText(mcomment.getAvtor2());
            if(mcomment.getStatus()==1)
            {
                jTextField4.setEnabled(true);
                jTextField5.setEnabled(true);
                jTextField2.setEnabled(false);
                jTextField3.setEnabled(false);
            }            
            if(mcomment.getStatus()>1)
            {
                jTextField4.setEnabled(false);
                jTextField5.setEnabled(false);
                jTextField2.setEnabled(false);
                jTextField3.setEnabled(false);
            }
        }
        if(mcomment.getModel().getModel()>0)        
        {
            jButton3.setEnabled(false);
            mcomment.setModel(IOModel.getListModel(mcomment.getModel().getModel(), -1, false).get(0));
            jTextField1.setText(mcomment.getModel().getArticle());            
        }
        
        
    }//GEN-LAST:event_formWindowOpened

