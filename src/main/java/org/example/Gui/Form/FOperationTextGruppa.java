    // добавить
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        FAddName fa = new FAddName(null, true, "");
        fa.setVisible(true);
        if(fa.getResult())
        {
            OperationTextGruppaJournal gruppaJournal = new OperationTextGruppaJournal();
            gruppaJournal.setOperationTextGruppa(operationTextGruppa);
            gruppaJournal.setText(fa.getText());
            gruppaJournal.setOperationtextgruppajournal(0);
            gruppaJournal.setNom(jTable1.getRowCount()+1);
            IOOperationTextGruppa.setOperationTextGruppaJournal(gruppaJournal);
            model.setData(operationTextGruppa.getOperationtextgruppa());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    // удалить
    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        int h = jTable1.getSelectedRow();
        if(h==-1)
            return;
        h = (int) jTable1.getValueAt(h, 0);
        if (JOptionPane.showConfirmDialog(null, "Вы действительно хотите удалить <" + jTable1.getValueAt(jTable1.getSelectedRow(), 2) + ">?", "Вопрос.", JOptionPane.YES_NO_OPTION) == 0) 
        {
            if(IOOperationTextGruppa.delOperationTextGruppaJournal(h))
                model.setData(operationTextGruppa.getOperationtextgruppa());
            else
                JOptionPane.showMessageDialog(null, "Ошибка удаления");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

