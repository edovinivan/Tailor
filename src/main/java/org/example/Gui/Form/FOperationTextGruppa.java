    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int h = jTable1.getSelectedRow();
        if(h==-1)
            return;
        h = (int) jTable1.getValueAt(h, 0);
        OperationTextGruppaJournal gruppaJournal = IOOperationTextGruppa.getOperationTextGruppaJournal(h);
        
        FAddName fa = new FAddName(null, true, gruppaJournal.getText());
        fa.setVisible(true);
        if(fa.getResult())
        {            
            gruppaJournal.setText(fa.getText());
            IOOperationTextGruppa.setOperationTextGruppaJournal(gruppaJournal);
            model.setData(operationTextGruppa.getOperationtextgruppa());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

