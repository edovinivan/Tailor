    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(operationTextGruppa.getOperationtextgruppa() == 0){
            if(jTextField1.getText()==null || jTextField1.getText().length()< 3){                
                return;
            }
            operationTextGruppa.setDel(0);            
            operationTextGruppa.setName(jTextField1.getText());
            operationTextGruppa = IOOperationTextGruppa.setOperationTextGruppa(operationTextGruppa);
            jButton3.setEnabled(operationTextGruppa.getOperationtextgruppa() != 0);
            jButton4.setEnabled(operationTextGruppa.getOperationtextgruppa() != 0);
            jButton5.setEnabled(operationTextGruppa.getOperationtextgruppa() != 0);
            result = true;
        } else {
            operationTextGruppa.setName(jTextField1.getText());
            operationTextGruppa = IOOperationTextGruppa.setOperationTextGruppa(operationTextGruppa);
            result = true;
            setVisible(false);
                    
        }
         
    }//GEN-LAST:event_jButton1ActionPerformed

