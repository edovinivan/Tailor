    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        FGetRabotnik f = new FGetRabotnik(null, true, oper.getRabotnik().getRabotnik(), -1);
        f.setVisible(true);
        if(f.getResult())
        {
            oper.setRabotnik(IORabotnik.getListRabotnik(f.getResultValue()).get(0));
            jTextField3.setText(oper.getRabotnik().getName());            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

