    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!HelpClass.checkTextForName(jTextField1.getText()))
        {
            JOptionPane.showMessageDialog(null, "Введите ФИО");
            return;
        }
        oper.setDel(0);
        oper.setName(jTextField1.getText());
        oper.setPasswd(Integer.parseInt(jTextField2.getText()));
        if(IOOperator.setOperator(oper).getOperator()>0)
        {
            IOOperator.setOperatorSetup(model.getOS(oper.getOperator()));
            setVisible(false);
        }
        else
            JOptionPane.showMessageDialog(null, "Ошибка сохранения!");
    }//GEN-LAST:event_jButton1ActionPerformed

