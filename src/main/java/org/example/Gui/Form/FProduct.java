    private void getProduct() {
        prod = IOProduct.getListProduct(prod.getProduct(), 0).get(0);
        jTextField1.setText(prod.getName());
        jTextField3.setText(prod.getMeasure().getName());
        jTextField2.setText(prod.getKoef().setScale(2, RoundingMode.HALF_EVEN).toString());
        jComboBox1.getModel().setSelectedItem(prod.getProductgruppa());
    }
    
