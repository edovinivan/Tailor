    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jTextField1.getText() == null)
        {
            JOptionPane.showMessageDialog(null, "Введите название!");
            return;
        }
        if(jTextField1.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Введите название!");
            return;
        }
        if(jTextField1.getText().length()< 2)
        {
            JOptionPane.showMessageDialog(null, "Введите название!");
            return;
        }
        if(prod.getMeasure().getMeasure()==0)
        {
            JOptionPane.showMessageDialog(null, "Выберите единицу измерения!");
            return;
        }
        prod.setName(jTextField1.getText());
        prod.setDel(0);
        prod.setProductgruppa(((ProductGruppa)jComboBox1.getSelectedItem()));
        prod.setKoef(new BigDecimal(jTextField2.getText().replace(",", ".")));        
        prod = IOProduct.setProduct(prod);
        result = prod.getProduct()>0;
        if(!result)
            JOptionPane.showMessageDialog(null, "Ошибка созранения!");
        else
            setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

