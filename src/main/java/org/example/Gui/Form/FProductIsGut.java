    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        //длина
        if(!HelpClass.checkTextForBigDecimal(jTextField2.getText().replace(",",".")))
        {
            JOptionPane.showMessageDialog(null, "Введите цену правильно!");
            return;
        }
        sp.setPrice(new BigDecimal(jTextField2.getText().replace(",",".")));

        sp.setWidth(new BigDecimal((int)jSpinner2.getValue()));
        if(sp.getWidth().signum()==0)
        {
            JOptionPane.showMessageDialog(null, "Введите ширину правильно!");
            return;
        }

        
        //длина
        if(!HelpClass.checkTextForBigDecimal(jTextField6.getText().replace(",",".")))
        {
            JOptionPane.showMessageDialog(null, "Введите длину правильно!");
            return;
        }
        sp.setLight(new BigDecimal(jTextField6.getText().replace(",",".")));
        if(sp.getLight().signum()==0)
        {
            JOptionPane.showMessageDialog(null, "Введите длину правильно!");
            return;
        }
        
        //вес
        if(!HelpClass.checkTextForBigDecimal(jTextField9.getText().replace(",",".")))
        {
            JOptionPane.showMessageDialog(null, "Введите вес правильно!");
            return;
        }
        sp.setVes(new BigDecimal(jTextField9.getText().replace(",",".")));
        if(sp.getVes().signum()==0)
        {
            JOptionPane.showMessageDialog(null, "Введите вес правильно!");
            return;
        }
        
        sp.setPasport(jTextField3.getText());
        sp.setStatus(1);

        IOScladProduct.setScladProduct(sp);
        
        setVisible(false);
    }//GEN-LAST:event_jButton2ActionPerformed

