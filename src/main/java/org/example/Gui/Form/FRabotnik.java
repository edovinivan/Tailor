    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jTextField1.getText() == null)
        {
            JOptionPane.showMessageDialog(null, "Введите название!");
            return;
        }
        if(jTextField1.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Введите название!");
            return;
        }
        if(jTextField1.getText().length()< 2)
        {
            JOptionPane.showMessageDialog(null, "Введите название!");
            return;
        }
        
        if(jTextField2.getText() == null)
        {
            JOptionPane.showMessageDialog(null, "Введите коэффициент!");
            return;
        }
        if(jTextField2.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Введите коэффициент!");
            return;
        }
        if(jTextField2.getText().length()< 1)
        {
            JOptionPane.showMessageDialog(null, "Введите  коэффициент!");
            return;
        }
        
        rab.setPrice(HelpClass.GetBigDecimalIzText(jTextField3.getText()));
        
        rab.setDel(0);
        rab.setKoef(new BigDecimal(jTextField2.getText().replace(",", ".")));
        rab.setName(jTextField1.getText());
        rab.setRabotnikgruppa((RabotnikGruppa) jComboBox1.getSelectedItem());
        rab = IORabotnik.setRabotnik(rab);
        if(rab.getRabotnik()>0)
        {
            result = true;
            setVisible(false);
        }
        else
            JOptionPane.showMessageDialog(null, "Ошибка сохранения!");
    }//GEN-LAST:event_jButton1ActionPerformed

