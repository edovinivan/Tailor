    private void loadNull() {
        for(RabotnikGruppa a: IORabotnik.getListRabotnikGruppa(-1))            
            jComboBox1.addItem(a);   
        jComboBox1.setSelectedIndex(0);
    }

    private void loadRab() {
        rab = IORabotnik.getListRabotnik(rab.getRabotnik()).get(0);
        jTextField1.setText(rab.getName());
        jTextField2.setText(rab.getKoef().setScale(2, RoundingMode.HALF_UP).toString());
        jComboBox1.getModel().setSelectedItem(rab.getRabotnikgruppa());        
        jTextField3.setText(rab.getPrice().setScale(2, RoundingMode.HALF_UP).toString());
    }
    
