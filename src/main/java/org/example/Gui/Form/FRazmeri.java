    private void loadRazmeri() {
        razmer = IORazmeri.getListRazmeri(razmer.getRazmeri()).get(0);
        jTextField1.setText(razmer.getName());
        jComboBox1.getModel().setSelectedItem(razmer.getRazmerigruppa());
        jSpinner1.setValue(razmer.getNom());
    }
    
    /**
     * загрузить начальные данные
     */
    private void loadNullValue()
    {        
        for(RazmeriGruppa a: IORazmeri.getListRazmeriGruppa(-1))            
            jComboBox1.addItem(a);   
        jComboBox1.setSelectedIndex(0);
        jSpinner1.setValue(0);
    }
    
