    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jTextField1.getText() == null)
        {
            JOptionPane.showMessageDialog(null, "Введите название!");
            return;
        }
        if(jTextField1.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Введите название!");
            return;
        }
        if(jTextField1.getText().length()< 2)
        {
            JOptionPane.showMessageDialog(null, "Введите название!");
            return;
        }
        razmer.setName(jTextField1.getText());
        razmer.setDel(0);
        razmer.setNom((int)jSpinner1.getValue());
        razmer.setRazmerigruppa((RazmeriGruppa) jComboBox1.getSelectedItem());
        
        
        razmer = IORazmeri.setRazmeri(razmer);
        result = razmer.getRazmeri() > 0;
        if(razmer.getRazmeri() > 0 )
            setVisible(false);
        else
            JOptionPane.showMessageDialog(null, "Ошибка сохранения!");
            
    }//GEN-LAST:event_jButton1ActionPerformed

