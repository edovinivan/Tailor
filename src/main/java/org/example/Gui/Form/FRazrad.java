    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!HelpClass.checkTextForName(jTextField1.getText()))
        {
            JOptionPane.showMessageDialog(null, "Введите название");
            return;
        }
        
        if(!HelpClass.checkTextForBigDecimal(jTextField2.getText()))
        {
            JOptionPane.showMessageDialog(null, "Введите цену");
            return;
        }
        
        razrad.setDel(0);
        razrad.setNom(jTextField1.getText());
        razrad.setSumma(HelpClass.GetBigDecimalIzText(jTextField2.getText()));
        razrad =  IORazrad.setRazrad(razrad);
        if(razrad.getRazrad()<=0)
        {
            JOptionPane.showMessageDialog(null, "Ошибка сохранения!");
            return;
        }
        setVisible(false);
        
    }//GEN-LAST:event_jButton1ActionPerformed

