    private boolean saveRevizia()
    {
        if(!HelpClass.checkTextForName(jTextField1.getText()))
        {
            JOptionPane.showMessageDialog(null, "Введите номер!");
            return false;
        }
        
        rev.setNomer(jTextField1.getText());        
        rev.setDates(jDateChooser1.getDate());        
        rev.setStatus(0);                     
        rev = IORevizia.setRevizia(rev);        
        return true;
    }

    private void loadRevizia() {
        rev = IORevizia.getRevizia(rev.getRevizia());
        jTextField1.setText(rev.getNomer());
        jDateChooser1.setDate(rev.getDates());
        //если ревизия закрыта
        if(rev.getStatus()>0)
        {
            jButton1.setEnabled(false);
            jButton3.setEnabled(false);
            jButton4.setEnabled(false);
            jButton5.setEnabled(false);
            jButton6.setEnabled(false);
            jButton9.setEnabled(false);
            jCheckBox1.setEnabled(false);
            model.stop_edit = false;
        }
    }
