    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!HelpClass.checkTextForName(jTextField1.getText()))
        {
            JOptionPane.showMessageDialog(null, "Введите название!");
            return;
        }

        tm.setKoef(HelpClass.GetBigDecimalIzText(jTextField2.getText()));
        if(tm.getKoef().signum()<=0)
        {
            JOptionPane.showMessageDialog(null, "Введите стоимость правильно!");
            return;
        }
        tm.setName(jTextField1.getText());
        tm.setDel(0);
        tm = IOTipModel.setTipModel(tm);
        if(tm.getTipmodel()<0)
        {
            JOptionPane.showMessageDialog(null, "Ошибка сохранения!");
            return;
        }
        result = true;
        setVisible(false);
        
    }//GEN-LAST:event_jButton1ActionPerformed

