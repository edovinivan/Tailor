    private void jTable1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable1KeyPressed
        // TODO add your handling code here:    
        switch(evt.getKeyCode())
        {
            case KeyEvent.VK_ESCAPE:
                jLabel1.setText("");
                model.setData(status,0,0,0,0,0, jLabel1.getText());
                break;
            case KeyEvent.VK_BACK_SPACE:
                if(jLabel1.getText().length()>0)
                    jLabel1.setText(jLabel1.getText().substring(0, jLabel1.getText().length()-1));
                break; 
            case KeyEvent.VK_ENTER:
                model.setData(status,0,0,0,0,0, jLabel1.getText());
                break;
            default:            
                if(String.valueOf(evt.getKeyChar()).matches("[a-zA-Zа-яА-Я0-9\\,\\.]"))                                 
                    jLabel1.setText(jLabel1.getText()+String.valueOf(evt.getKeyChar()));
        }
    }//GEN-LAST:event_jTable1KeyPressed

