    /**
     * Построение окна
     * @param s 
     */
    private void createWindow(int s, int vid)
    { 
        int w,h;
        w = Toolkit.getDefaultToolkit ().getScreenSize().width-20;
        h = Toolkit.getDefaultToolkit ().getScreenSize().height-50;
        setSize(w,h);      
        setLocationRelativeTo(null);
        //setSize(Toolkit.getDefaultToolkit ().getScreenSize());         
        //pack();
        model = new ModelScladProduct(vid);
        result_id = s;
        addWindowListener(new java.awt.event.WindowAdapter() {
            @Override
            public void windowOpened(java.awt.event.WindowEvent evt) {
                openWindows();
            }
        });
        
        jTable1.addComponentListener(new java.awt.event.ComponentAdapter() {
            @Override
            public void componentResized(java.awt.event.ComponentEvent evt) {
                widthTable();
            }
        });
        
        jButton1.addActionListener(new java.awt.event.ActionListener() {
            @Override
            public void actionPerformed(java.awt.event.ActionEvent evt) {
                setVisible(false);
            }
        });
        
        jButton2.addActionListener(new java.awt.event.ActionListener() {
            @Override
            public void actionPerformed(java.awt.event.ActionEvent evt) {
                getClickOk();
            }
        });
        
    }
    
    private void openWindows()
    {
        setLocationRelativeTo(null);
        model.setData(status,0,0,agent,0,0, jLabel1.getText());
        jTable1.setModel(model);
        jTable1.setShowGrid(true);                    
        jTable1.getTableHeader().setReorderingAllowed(false);   
        
        jTable1.getColumnModel().getColumn(4).setCellRenderer(HelpClass.jTableRendererColumnRight);
        jTable1.getColumnModel().getColumn(5).setCellRenderer(HelpClass.jTableRendererColumnRight);
        
        widthTable();
        jTable1.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                jTable1MouseClicked(evt);
            }

            private void jTable1MouseClicked(MouseEvent evt) {
                if(evt.getClickCount() == 2)
                    getClickOk();
            }
        });
        if(result_id>0)
        {
            for(int i =0;i<jTable1.getRowCount();i++)
            {
                if((int)jTable1.getValueAt(i, 12) == result_id)
                {
                    jTable1.changeSelection(i, 0, false, false);
                    break;
                }
            }
        }
    }
    
    private void getClickOk()
    {
        int h = jTable1.getSelectedRow();
        if(h==-1) return;
        result_id = (int)jTable1.getValueAt(h, 12);
        result = true;
        setVisible(false);
    }

