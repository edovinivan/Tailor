    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int h = jTable1.getSelectedRow();
        if(h==-1)
            return;
        h = (int) jTable1.getValueAt(h, 0);
        if (JOptionPane.showConfirmDialog(null, "Вы действительно хотите удалить операцию <" + jTable1.getValueAt(jTable1.getSelectedRow(), 2)+ ">" + "?", "Вопрос.", JOptionPane.YES_NO_OPTION) == 0) 
        {
            if(IOModel.delModelOperation(h))
                loadData();//model.setData(mod);
            else
                JOptionPane.showMessageDialog(null, "Ошибка удаления");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        if (JOptionPane.showConfirmDialog(null, "Вы действительно хотите закрыть конфекционную карту для изменения операций?", "Вопрос.", JOptionPane.YES_NO_OPTION) == 0) 
        {
            Model m = IOModel.getListModel(mod, 0,true).get(0);
            if(m.getStatus()==0)
            {
                JOptionPane.showMessageDialog(null, "Данная конфекционная карта еще утверждена");
                return;
            }
            if(m.getStatus()==2)
                return;
            
                        m.setStatus(2);
            IOModel.setModel(m);
            jButton2.setEnabled(false);
            jButton3.setEnabled(false);
            jButton4.setEnabled(false);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        if (JOptionPane.showConfirmDialog(null, "Вы действительно хотите открыть конфекционную карту для изменения операций?", "Вопрос.", JOptionPane.YES_NO_OPTION) == 0) 
        {
            Model m = IOModel.getListModel(mod, 0, true).get(0);            
            if(m.getStatus()!=2)
                return;
            // проверим не отправили ли маршрус этой кк в цех
            if(IOModel.getModelForZadachaKomplektovka(m.getModel()))
            {
                JOptionPane.showMessageDialog(null, "Данная конфекционная карта уже используется в маршрутах! Отменить операции нельзя!");
                return;
            }
            

            
            m.setStatus(1);
            IOModel.setModel(m);
            jButton2.setEnabled(true);
            jButton3.setEnabled(true);
            jButton4.setEnabled(true);
        }
    }//GEN-LAST:event_jButton7ActionPerformed

