    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        JFileChooser jf = new JFileChooser();        
        if(jf.showOpenDialog(this) == JFileChooser.APPROVE_OPTION)
        {
           jTextField2.setText(jf.getSelectedFile().getAbsolutePath());
           file = new File(jf.getSelectedFile().getAbsolutePath());
           downloadNow = true;
           if(jTextField1.getText().isEmpty())
           {
               jTextField1.setText(file.getName());
           }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!HelpClass.checkTextForName(jTextField1.getText()))
        {
            JOptionPane.showMessageDialog(null, "Введите название");
            return;
        }
        if(downloadNow)        
        {
            Files fi = report.getFiles();
            
            fi.setObj(IOFiles.loadFile(file.getAbsolutePath()));
            fi.setName(file.getName());            
            report.setFiles(IOFiles.setFiles(fi));
            
        }
        report.setName(jTextField1.getText());            
        
        report = IOReport.setReport(report);
        result = report.getReport() != 0;
        setVisible(false);        
    }//GEN-LAST:event_jButton1ActionPerformed

