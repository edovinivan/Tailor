    /**
     * открыть документ
     * @param evt 
     */
    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        int h = jTable1.getSelectedRow();
        if(h==-1)
            return;
        //h = ((BigInteger) jTable1.getValueAt(h, 0)).intValue();
        h = (int) jTable1.getValueAt(h, 0);
        if (JOptionPane.showConfirmDialog(null, "Вы действительно хотите отменить отработку документа № <" + jTable1.getValueAt(jTable1.getSelectedRow(), 2) + ">?", "Вопрос.", JOptionPane.YES_NO_OPTION) == 0) 
        {
            int r = IODocument.Document_open(h);
            if(r==-2)
            {
                JOptionPane.showMessageDialog(null, "По товарам данной накладной уже были списания! Открытие невозможно!");
                return;
            }            
            
            if(r==-3)
            {
                JOptionPane.showMessageDialog(null, "По товарам данной накладной уже было движение! Открытие невозможно!");
                return;
            }
            model.setData(d1, d2);            
        }
    }//GEN-LAST:event_jButton8ActionPerformed

