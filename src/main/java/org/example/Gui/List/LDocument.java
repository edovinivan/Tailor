    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        int h = jTable1.getSelectedRow();
        if(h==-1)
            return;
        h = (int) jTable1.getValueAt(h, 0);
        if (JOptionPane.showConfirmDialog(null, "Вы действительно хотите провести документ № <" + jTable1.getValueAt(jTable1.getSelectedRow(), 2) + ">?", "Вопрос.", JOptionPane.YES_NO_OPTION) == 0) 
        {
            int r = IODocument.Document_close(h);
            if(r == 1)
                model.setData(d1, d2);
            else
            {
                switch(r)
                {
                    case -1:
                        JOptionPane.showMessageDialog(null, "Системная ошибка проведения");
                        break;
                    case -3:
                        JOptionPane.showMessageDialog(null, "Документ уже отработан!");
                        break;    
                    case -2:
                        JOptionPane.showMessageDialog(null, "Ошибка! Документ не имеет строк!");
                        break;        
                    default:
                        JOptionPane.showMessageDialog(null, "Ошибка проведения!" + r);
                }
            }
        }
    }//GEN-LAST:event_jButton7ActionPerformed

