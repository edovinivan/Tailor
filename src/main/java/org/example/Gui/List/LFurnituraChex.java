    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        int h = jTable1.getSelectedRow();
        if(h==-1) return;
        if(((RabotnikGruppa)jComboBox1.getModel().getSelectedItem()).getRabotnikgruppa() == 0)
        {
            JOptionPane.showMessageDialog(null, "Выберите цех");
            return;
        }
        FFurnituraOff ff = new FFurnituraOff(null, true, 0, ((RabotnikGruppa)jComboBox1.getModel().getSelectedItem()).getRabotnikgruppa());
        ff.setFurnitura((int)jTable1.getValueAt(h, 0));
        ff.setVisible(true);
        model.setData(((RabotnikGruppa)jComboBox1.getModel().getSelectedItem()).getRabotnikgruppa());        
    }//GEN-LAST:event_jButton3ActionPerformed


