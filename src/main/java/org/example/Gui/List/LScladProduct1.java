    /**
     * Назначить фильтр
     * @param product - товар
     * @param color - цвет
     */
    public void setFilter(int product, int color)
    {
        jToggleButton1.setSelected(true);
        jToggleButton1ActionPerformed(null);
        boolean f_p = true;
        boolean f_c = true;
        
        // товар
        for(int i=0;i<jComboBox1.getItemCount();i++)//  ComboItem c: jComboBox1.getItemAt(vid))
        {
            if(((ComboItem)jComboBox1.getItemAt(i)).getId() == product)
            {
                jComboBox1.setSelectedIndex(i);
                f_p = false;
                break;
            }
        }
            
        // цвет
        for (int i = 0; i < jComboBox2.getItemCount(); i++)//  ComboItem c: jComboBox1.getItemAt(vid))
        {
            if (((ComboItem) jComboBox2.getItemAt(i)).getId()== color) 
            {
                jComboBox2.setSelectedIndex(i);
                f_c = false;
                break;
            }
        }
        
        // если такого полотна нет
        if(f_p)
        {
            if(vid == 1)
                JOptionPane.showMessageDialog(null, "Такого материала нет на складе!");
            else
                JOptionPane.showMessageDialog(null, "Такой фурнитуры нет на складе!");
        }
        
        // если такого цвета нет
        if(f_c)
            JOptionPane.showMessageDialog(null, "Такого цвета нет на складе!");
        
    }
    
