    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        // если двойное нажатие
        if(evt.getClickCount() == 2)
        {
            if(jTable1.getSelectedRow() ==-1)
                return;
            int row = jTable1.getSelectedRow();
            int col = jTable1.getSelectedColumn();
            String s;
            // товар
            if(col == 2)            
            {
                s = (String)jTable1.getValueAt(row, col);
                for(int i=0;i<jComboBox1.getItemCount();i++)//  ComboItem c: jComboBox1.getItemAt(vid))
                {
                    if(((ComboItem)jComboBox1.getItemAt(i)).getName().equals(s))
                    {
                        jComboBox1.setSelectedIndex(i);
                        break;
                    }
                }
                //System.out.println(s);
            }
            
            // цвет
            if(col == 3)            
            {
                s = (String)jTable1.getValueAt(row, col);
                for(int i=0;i<jComboBox2.getItemCount();i++)//  ComboItem c: jComboBox1.getItemAt(vid))
                {
                    if(((ComboItem)jComboBox2.getItemAt(i)).getName().equals(s))
                    {
                        jComboBox2.setSelectedIndex(i);
                        break;
                    }
                }
            }
            
            // место хранения
            if(col == 8)            
            {
                s = (String)jTable1.getValueAt(row, col);
                for(int i=0;i<jComboBox5.getItemCount();i++)//  ComboItem c: jComboBox1.getItemAt(vid))
                {
                    if(((ComboItem)jComboBox5.getItemAt(i)).getName().equals(s))
                    {
                        jComboBox5.setSelectedIndex(i);
                        break;
                    }
                }
            }
            
            // поставщик
            if(col == 10)            
            {
                s = (String)jTable1.getValueAt(row, col);                
                for(int i=0;i<jComboBox4.getItemCount();i++)//  ComboItem c: jComboBox1.getItemAt(vid))
                {
                    if(((ComboItem)jComboBox4.getItemAt(i)).getName().equals(s))
                    {
                        jComboBox4.setSelectedIndex(i);
                        break;
                    }
                }
            }
            
            // ширина
            if(col == 5)            
            {
                if(vid!=1)return;
                s = String.valueOf((int)jTable1.getValueAt(row, col));
                //System.out.println("ssssss " + s);
                
                for(int i=0;i<jComboBox3.getItemCount();i++)
                {
                    if(((ComboItem)jComboBox3.getItemAt(i)).getName().equals(s))
                    {
                        jComboBox3.setSelectedIndex(i);
                        break;
                    }
                }
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

