    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        FGetModel fg= new FGetModel(null, true, mapProjectJournal.getModel().getModel(), false);        
        fg.setVisible(true);
        if(fg.getResult())
        {            
            mapProjectJournal.setModel(IOModel.getListModel(fg.getResultValue(), -1, false).get(0));            
            jTextField1.setText(mapProjectJournal.getModel().getArticle());                                 
            List<ModelProduct> ls = IOModel.getListModelProduct(mapProjectJournal.getModel().getModel() , -1, 1);
            for(ModelProduct m: ls){
                if(m.getTipproduct().getTipproduct()==1){
                    mapProjectJournal.setColor(m.getColor());
                    jTextField2.setText(mapProjectJournal.getColor().getName());
                    break;
                }
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        setLocationRelativeTo(null);
        setResizable(false);
        jDateChooser1.setDate(new Date());
        
        if(mapProjectJournal.getMapprojectjournal() != 0){
            mapProjectJournal = iopj.getObject(mapProjectJournal.getMapprojectjournal());
            jDateChooser1.setDate(mapProjectJournal.getDates());
            jTextField1.setText(mapProjectJournal.getModel().getArticle());
            jTextField2.setText(mapProjectJournal.getColor().getName());
            jTextField3.setText(mapProjectJournal.getRazmeri().getName());
            jSpinner1.setValue(mapProjectJournal.getQty());
        } else {            
            mapProjectJournal.setModel(new Model(0));
            mapProjectJournal.setColor(new Color(0));
            mapProjectJournal.setRazmeri(new Razmeri(0));
            mapProjectJournal.setSale(0);
            mapProjectJournal.setInwork(0);
            mapProjectJournal.setBalance(0);
            mapProjectJournal.setSalemoney(0);
        }
        
    }//GEN-LAST:event_formWindowOpened

