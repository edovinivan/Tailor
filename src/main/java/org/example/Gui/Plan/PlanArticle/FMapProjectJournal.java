    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(mapProjectJournal.getModel().getModel()==0){
            JOptionPane.showMessageDialog(null, "Выберите артикул!");
            return;
        }       
        
        if(mapProjectJournal.getColor().getColor()==0){
            JOptionPane.showMessageDialog(null, "Выберите цвет!");
            return;
        }
        
        if(mapProjectJournal.getRazmeri().getRazmeri()==0){
            JOptionPane.showMessageDialog(null, "Выберите размер!");
            return;
        }
        
        mapProjectJournal.setQty((int)jSpinner1.getValue());
        if(mapProjectJournal.getQty() <1 ){
            JOptionPane.showMessageDialog(null, "Введите количество!");
            return;
        }
        
        mapProjectJournal.setDates(jDateChooser1.getDate());
        
        iopj.setObject(mapProjectJournal);
        result = true;
        setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        if(mapProjectJournal.getModel().getModel()==0) return;
        FGetRazmeri fg = new FGetRazmeri(null, true, 0, mapProjectJournal.getModel().getModel());
        fg.setVisible(true);
        if(fg.getResult())
        {
            mapProjectJournal.setRazmeri(IORazmeri.getListRazmeri(fg.getResultValue()).get(0));
            jTextField3.setText(mapProjectJournal.getRazmeri().getName());                     
        }
    }//GEN-LAST:event_jButton5ActionPerformed

