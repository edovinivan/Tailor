    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        FGetModel fg= new FGetModel(null, true, planarticle.getModel().getModel());        
        fg.setVisible(true);
        if(fg.getResult())
        {            
            planarticle.setModel(IOModel.getListModel(fg.getResultValue(), -1, false).get(0));            
            jTextField1.setText(planarticle.getModel().getArticle());                                 
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        setLocationRelativeTo(null);
        setResizable(false);
        jDateChooser1.setDate(new Date());
        
        if(planarticle.getPlanarticle() != 0){
            planarticle = IOPlanArticle.getPlanArticle(planarticle.getPlanarticle());            
            jDateChooser1.setDate(planarticle.getDates());
            jTextField1.setText(planarticle.getModel().getArticle());
            jTextField2.setText(planarticle.getColor().getName());
            jSpinner1.setValue(planarticle.getQty());
        } else {
            planarticle.setPlanarticle(0);
            planarticle.setModel(new Model(0));
            planarticle.setColor(new Color(0));
        }
        
    }//GEN-LAST:event_formWindowOpened

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        FGetColor fg= new FGetColor(null, true, planarticle.getColor().getColor(), 1);        
        fg.setVisible(true);
        if(fg.getResult())
        {            
            planarticle.setColor(IOColor.getListColor(fg.getResultValue(), -1).get(0));            
            jTextField2.setText(planarticle.getColor().getName());                     
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(planarticle.getModel().getModel()==0){
            JOptionPane.showMessageDialog(null, "Выберите артикул!");
            return;
        }       
        
        if(planarticle.getColor().getColor()==0){
            JOptionPane.showMessageDialog(null, "Выберите цвет!");
            return;
        }
        
        planarticle.setQty((int)jSpinner1.getValue());
        if(planarticle.getQty() <1 ){
            JOptionPane.showMessageDialog(null, "Введите количество!");
            return;
        }
        
        planarticle.setDates(jDateChooser1.getDate());
        
        IOPlanArticle.setPlanArticle(planarticle);
        result = true;
        setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

