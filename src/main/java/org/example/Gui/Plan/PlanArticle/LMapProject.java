    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int h = jTable1.getSelectedRow();
        if(h==-1) return;
        if(JOptionPane.showConfirmDialog(null, "Вы действительно хотите удалить " + jTable1.getValueAt(h, 2), "Вопрос", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
            IOMapProject iOMapProject = new IOMapProject();
            iOMapProject.delObject((int)jTable1.getValueAt(h, 0));
            model.setData(d1, d2);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

