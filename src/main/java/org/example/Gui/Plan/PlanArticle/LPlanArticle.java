    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int h = jTable1.getSelectedRow();
        if(h==-1)
            return;
        h = (int) jTable1.getValueAt(h, 0);
        if (JOptionPane.showConfirmDialog(null, "Вы действительно хотите удалить план задачи?", "Вопрос.", JOptionPane.YES_NO_OPTION) == 0) 
        {
            IOPlanArticle.delPlanArticle(h);
            model.setData(d1, d2);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

