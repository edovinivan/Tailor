    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        setLocationRelativeTo(null);
        jDateChooser1.setDate(new Date());
        loadNull();
        if(ud.getUdocument()>0)
        {
            ud = IOUchet.getUDocument(ud.getUdocument());
            jTextField1.setText(ud.getSumma().setScale(2).toString());
            jTextField2.setText(ud.getTexts());
            jComboBox1.setSelectedItem(ud.getUoperation());
            jDateChooser1.setDate(ud.getDates());
        }
    }//GEN-LAST:event_formWindowOpened

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!HelpClass.checkTextForBigDecimal(jTextField1.getText()))
        {
            JOptionPane.showMessageDialog(null, "Введите сумму!");
            return;
        }
        ud.setDates(jDateChooser1.getDate());
        ud.setSumma(HelpClass.GetBigDecimalIzText(jTextField1.getText()));
        ud.setTexts(jTextField2.getText());
        ud.setUdates(new Date());
        ud.setUoperation((UOperation)jComboBox1.getModel().getSelectedItem());
        IOUchet.setUDocument(ud);
        setVisible(false);        
    }//GEN-LAST:event_jButton1ActionPerformed

