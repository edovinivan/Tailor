    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        UOperJou u = new UOperJou();
        
        u.setComment("Авто дополнение часа");
        u.setDates(new Date());
        
        Calendar c = Calendar.getInstance();
        c.add(Calendar.HOUR_OF_DAY, -1);        
        u.setBdates(c.getTime());
        u.setEdates(new Date());
        u.setUoperdoc(uo);
        u.setUoperjou(0);
        IOUOperDoc.setUOperJou(u);
        model.setData(uo.getUoperdoc());
    }//GEN-LAST:event_jButton8ActionPerformed

    private boolean saveDoc()
    {
        if(!HelpClass.checkTextForName(jTextField1.getText()))
        {
            JOptionPane.showMessageDialog(null, "Введите название!");
            return false;
        }
        if(uo.getUoperdoc()==0)
        {
            uo.setComms(jTextField2.getText());
            uo.setDates(new Date());
            uo.setName(jTextField1.getText());
            uo.setOperator(IOOperator.operator);
            uo.setStatus(0);            
        }
        else
        {
            uo.setComms(jTextField2.getText());            
            uo.setName(jTextField1.getText());
        }
        uo = IOUOperDoc.setUOperDoc(uo);
        return true;
    }
    
