    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        setLocationRelativeTo(null);
        if(uj.getUoperjou()!=0)
        {
            uj = IOUOperDoc.getUOperJou(uj.getUoperjou());
            jDateChooser1.setDate(uj.getBdates());
            jDateChooser2.setDate(uj.getEdates());            
            jTextField1.setText(uj.getComment());
        }
        else
        {
            jDateChooser1.setDate(new Date());
            jDateChooser2.setDate(new Date());            
        }
        if(go_start)
            jLabel3.setText("Название задачи");
    }//GEN-LAST:event_formWindowOpened

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(!go_start)
        {
            uj.setBdates(jDateChooser1.getDate());
            uj.setComment(jTextField1.getText());
            uj.setDates(new Date());
            uj.setEdates(jDateChooser2.getDate());
            IOUOperDoc.setUOperJou(uj);
            result = true;
            setVisible(false);
        }
        else
        {
            if (!HelpClass.checkTextForName(jTextField1.getText())) 
            {
                JOptionPane.showMessageDialog(null, "Введите название!");
                return;
            }
            UOperDoc d = new UOperDoc();
            d.setComms("");
            d.setDates(new Date());
            d.setName(jTextField1.getText());
            d.setOperator(IOOperator.operator);
            d.setStatus(1);
            d.setUoperdoc(0);
            d = IOUOperDoc.setUOperDoc(d);
            UOperJou j = new UOperJou();
            j.setBdates(jDateChooser1.getDate());
            j.setComment(jTextField1.getText());
            j.setDates(new Date());
            j.setEdates(jDateChooser2.getDate());
            j.setUoperdoc(d);
            j.setUoperjou(0);
            IOUOperDoc.setUOperJou(j);
            result = true;
            setVisible(false);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

