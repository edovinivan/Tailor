    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        setLocationRelativeTo(null);
        if(op.getUoperation()>0)
        {
            op = IOUchet.getUOperation(op.getUoperation());
            jTextField1.setText(op.getName());
            if(op.getPm()==1)
                jRadioButton1.setSelected(true);
            else
                jRadioButton2.setSelected(true);
        }
    }//GEN-LAST:event_formWindowOpened

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!HelpClass.checkTextForName(jTextField1.getText()))
        {
            JOptionPane.showMessageDialog(null, "Введите название!");
            return;
        }
        op.setDel(0);
        op.setName(jTextField1.getText());
        op.setPm(jRadioButton1.isSelected()?1:2);
        op = IOUchet.setUOperation(op);
        result = true;
        setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

