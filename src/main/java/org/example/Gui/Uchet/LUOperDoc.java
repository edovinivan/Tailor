    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        int h = jTable1.getSelectedRow();
        if(h==-1)
            return;
        int hh = (int) jTable1.getValueAt(h, 0);
        if (JOptionPane.showConfirmDialog(null, "Вы действительно хотите закрыть задачу?", "Вопрос.", JOptionPane.YES_NO_OPTION) == 0) 
        {
            UOperDoc ud = IOUOperDoc.getUOperDoc(hh);
            if(ud.getMinutest()==0)
            {
                JOptionPane.showMessageDialog(null, "Ошибка. В задаче нет времени выполнения.");
                return;
            }
            if(ud.getStatus()==1)
                return;
            ud.setStatus(1);
            IOUOperDoc.setUOperDoc(ud);            
            model.setData(d1, d2);
            jTable1.setRowSelectionInterval(h, h);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        int h = jTable1.getSelectedRow();
        if(h==-1)
            return;
        int hh = (int) jTable1.getValueAt(h, 0);
        if (JOptionPane.showConfirmDialog(null, "Вы действительно хотите открыть задачу?", "Вопрос.", JOptionPane.YES_NO_OPTION) == 0) 
        {
            UOperDoc ud = IOUOperDoc.getUOperDoc(hh);
            
            if(ud.getStatus()==0)
                return;
            ud.setStatus(0);
            IOUOperDoc.setUOperDoc(ud);            
            model.setData(d1, d2);
            jTable1.setRowSelectionInterval(h, h);
        }
    }//GEN-LAST:event_jButton7ActionPerformed

