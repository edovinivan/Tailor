package org.example.Gui.model;

import java.util.ArrayList;
import java.util.List;
import javax.swing.table.AbstractTableModel;
import org.example.logic.MProperty;
import org.example.util.IOMProperty;

/**
 *
 * @author ivan
 */
public class ModelMProperty   extends AbstractTableModel{
    List<MProperty> list = new ArrayList<>();
    List<String> listCol = new ArrayList<>();

    
    public ModelMProperty() {
        listCol.add("№");
        listCol.add("Название");        
    }   
    
    @Override
    public int getRowCount() {
        return list.size();
    }

    @Override
    public int getColumnCount() {
        return listCol.size();
    }

    @Override
    public Object getValueAt(int rowIndex, int columnIndex) {
        switch(columnIndex)
        {
            case 0: 
                return list.get(rowIndex).getMproperty();
            case 1: 
                return list.get(rowIndex).getName();
                
        }
        return null;
    }

    @Override
    public String getColumnName(int column) {
        return listCol.get(column);
    }
    
    public void setData()
    {
        list = IOMProperty.getListMProperty();
        fireTableDataChanged();
    }
    
}
